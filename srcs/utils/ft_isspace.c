#include "../../includes/minishell.h"

int ft_isspace(char c)
{
    if (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r')
    {
        return 1; // 공백 문자
    }
    return 0; // 공백 문자가 아님
}
